drop policy if exists "users_select_own_or_admin" on public.users;
create policy "users_select_own_or_admin"
on public.users
for select
to authenticated
using (
  "authUserId" = auth.uid()
  or public.is_admin()
);

drop policy if exists "users_insert_own_or_admin" on public.users;
create policy "users_insert_own_or_admin"
on public.users
for insert
to authenticated
with check (
  "authUserId" = auth.uid()
  or public.is_admin()
);

drop policy if exists "users_update_own_or_admin" on public.users;
create policy "users_update_own_or_admin"
on public.users
for update
to authenticated
using (
  "authUserId" = auth.uid()
  or public.is_admin()
)
with check (
  "authUserId" = auth.uid()
  or public.is_admin()
);

drop policy if exists "drivers_select_authenticated" on public.drivers;
create policy "drivers_select_authenticated"
on public.drivers
for select
to authenticated
using (true);

drop policy if exists "drivers_admin_write" on public.drivers;
create policy "drivers_admin_write"
on public.drivers
for all
to authenticated
using (public.is_admin())
with check (public.is_admin());

